num1 = float(input("Enter any number: "))
num2 = float(input("Enter a second number: "))

addition = num1 + num2
difference = num1 - num2
product = num1 * num2
division = num1 / num2

print(f'{num1} + {num2} = {addition}')
print(f'{num1} - {num2} = {difference}')
print(f'{num1} * {num2} = {product}')
print(f'{num1} / {num2} = {division}')